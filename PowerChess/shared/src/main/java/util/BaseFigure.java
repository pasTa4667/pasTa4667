package util;

public class BaseFigure {

    public final int figureID;
    public final String name;
    public final int MP;
    public final int AP;

    public BaseFigure(int figureID, String name, int MP, int AP) {
        this.figureID = figureID;
        this.name = name;
        this.MP = MP;
        this.AP = AP;
    }
}
